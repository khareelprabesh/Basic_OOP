public class calculator {
    public double num1;
    public double num2;
    public void add () {
        System.out.println(num1 + num2);
    }
    public void sub () {
        System.out.println(num1 - num2);
    }
    public void product () {
        System.out.println(num1 * num2);
    }
    public void divide () {
        System.out.println(num1 / num2);
    }
    public void remainder () {
        System.out.println(num1 % num2);
    }
}
